function hideMetro() {
    var x = document.getElementById("contMetronomo")
    if (x.style.display === "none") {
        x.style.display = "block"
    } else {
        x.style.display = "none"
    }
}

function hidePiano() {
    var x = document.getElementById("contPiano")
    if (x.style.display === "none") {
        x.style.display = "block"
    } else {
        x.style.display = "none"
    }
}